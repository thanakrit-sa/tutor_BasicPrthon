# โปรแกรมต้องการแสดงข้อมูลชื่อและอายุออกมาโดยการรับค่าจากผู้ใช้งาน

# สร้างตัวแปรรับค่าจากผู้ใช้งาน
name = input('คุณชื่ออะไร : ')
age = int(input('คุณอายุเท่าไหร่ : '))
# แสดงค่าที่ได้รับจากผู้ใช้งาน
print('คุณชือ : %s'%name)
print('คุณอายุ : %d'%age)


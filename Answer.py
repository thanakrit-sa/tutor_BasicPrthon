# โปรแกรมแกรมต้องการรู้ว่าค่าตัวเลขที่รับจากผู้ใช้งานมา มากกว่าหรือน้อยกว่า 50

# สร้างตัวแปรรับค่าจากผู้ใช้งาน
number = int(input('กรุณากรอกจำนวนเลขที่ต้องการ : '))
# สร้างคำสั่งเงื่อนไขทางเลือกขึ้นมา
if number > 50:
    print('จำนวนเลขที่ท่านกรอกมากกว่า 50')
else:
    print('จำนวนเลขที่ท่านกรอกน้อยกว่า 50')

